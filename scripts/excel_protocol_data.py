# CONDITION_TO_MODALITIES — for a given condition string (partial match), which modalities have protocol data
def get_protocols_for_slug(slug: str) -> dict:
    """Return {modality_key: protocol_dict} for all modalities with data for this condition slug."""
    result = {}
    cond_name = SLUG_TO_CONDITION.get(slug)
    if not cond_name:
        return result
    for mod_key, conds in MODALITY_PROTOCOLS.items():
        for cond_key, proto in conds.items():
            if cond_name.lower() in cond_key.lower() or cond_key.lower() in cond_name.lower():
                result[mod_key] = proto
                break
    return result

