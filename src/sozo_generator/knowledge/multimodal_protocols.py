def get_condition_protocols(condition_name: str) -> dict[str, dict]:
    """Return {modality: protocol_record} for a condition (case-insensitive partial match).

    Searches across all modalities and returns every record whose 'condition'
    field contains *condition_name* (case-insensitive substring match).
    """
    query = condition_name.lower()
    result: dict[str, dict] = {}
    for modality, records in PROTOCOLS.items():
        for record in records:
            if query in record.get("condition", "").lower():
                # If multiple records match within one modality, the last wins;
                # callers needing all matches should iterate PROTOCOLS directly.
                result[modality] = record
    return result


def list_modalities() -> list[str]:
    """Return the list of modality keys present in PROTOCOLS."""
    return list(PROTOCOLS.keys())


def get_modality_protocols(modality: str) -> list[dict]:
    """Return all protocol records for a given modality key (exact match).

    Returns an empty list if the modality is not found.
    """
    return PROTOCOLS.get(modality, [])
